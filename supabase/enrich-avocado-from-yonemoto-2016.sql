  a.id,
  s.slug,
  s.name_ja,
  s.name_en,
  s.origin,
  s.description,
  s.fruit_size,
  s.taste,
  s.texture,
  s.harvest_season,
  s.cold_hardiness,
  s.flowering_type,
  s.tree_vigor,
  s.difficulty,
  s.okinawa_suitability,
  s.container_suitability,
  s.beginner_suitability,
  s.kenyu_comment,
  s.public_notes,
  s.private_notes,
  true,
  false,
  now()
from source s
cross join avocado a
on conflict (fruit_id, slug) do update
set
  name_ja = excluded.name_ja,
  name_en = excluded.name_en,
  origin = excluded.origin,
  description = excluded.description,
