  container_suitability = excluded.container_suitability,
  beginner_suitability = excluded.beginner_suitability,
  kenyu_comment = excluded.kenyu_comment,
  public_notes = case
    when coalesce(public.cultivars.public_notes, '') like '%' || excluded.public_notes || '%' then public.cultivars.public_notes
    else concat_ws(E'\n', nullif(public.cultivars.public_notes, ''), excluded.public_notes)
  end,
  private_notes = case
